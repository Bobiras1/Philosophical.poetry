# Πλατωνικὸς ποιητικὸς διάλογος
# Δύο πλήρεις εκδοχές: ἀρχαῖα ἑλληνικά καὶ νέα ἑλληνικά

dialogue_ancient = """
ΜΑΘΗΤΗΣ:
Ὦ Διδάσκαλε, τί ἐστιν ἡ εὐδαιμονία;

ΤΡΙΑΝΤΑΦΥΛΛΙΑ:
Ἡ εὐδαιμονία, τέκνον, οὐκ ἐν τοῖς ἔξω, ἀλλ᾽ ἐν τῇ ψυχῇ ἐνδιαιτᾶται.

ἌΝΝΑ:
Ἄνευ ποιήσεως ἡ ψυχὴ ξηραίνεται·
ἐν λόγοις δὲ ἄνθος γίνεται.

ΧΡΙΣΤΙΝΑ:
Ἡ συγκίνησις ἄνεμος· ἔρχεται καὶ παρέρχεται·
ἡ διδασκαλία ῥίζαν ἔχει καὶ μένει.

ἈΛΕΞΑΝΔΡΑ:
Ἡ ἀλήθεια ἰσχὺν παρέχει·
ὁ φιλῶν τέχνην καὶ λόγον οὐ φοβεῖται.

ΜΑΘΗΤΗΣ:
ἆρα ἡ ποίησις διδάσκει, ἢ μόνον τέρπει;

ΤΡΙΑΝΤΑΦΥΛΛΙΑ:
Καὶ τὸ αὐτό, ὦ φίλε· παιδεία καὶ παραμυθία ἓν ἔργον.

ἌΝΝΑ:
Ὡς κῆπος ἐστὶν ἡ λογοτεχνία·
καρπὸν ἢ ἄνθος δίδωσι.

ΧΡΙΣΤΙΝΑ:
Καὶ τὸ ἄνθος δὲν εὐκαταφρόνητον·
ἄνευ τοῦτο, ἡ καρδία νεκρά.

ἈΛΕΞΑΝΔΡΑ:
Ἡ εὐδαιμονία ἀρετῆς καρπός ἐστιν.

ΜΑΘΗΤΗΣ:
Πῶς διδάσκεται ἡ ἀρετή;

ΤΡΙΑΝΤΑΦΥΛΛΙΑ:
Οὐ λόγοις μόνον, ἀλλὰ βίῳ καὶ ἔργοις.

ἌΝΝΑ:
Πᾶν ποίημα μικρὰ πράξις·
σπέρμα ἐν ταῖς ψυχαῖς τῶν ἀκουόντων.

ΧΡΙΣΤΙΝΑ:
Καὶ ὁ ἀναγνώστης ποιητὴς γίνεται·
ἄνευ αὐτοῦ, στίχοι κενὸι μένουσιν.

ἈΛΕΞΑΝΔΡΑ:
Ἡ ψυχὴ ἀναγινώσκουσα νέον σῶμα φορεῖ.

ΜΑΘΗΤΗΣ:
Ἐνόμιζον τὴν εὐδαιμονίαν ὑλικὸν εἶναι·
νῦν ὁρῶ ὁδὸν εἶναι.

ΤΡΙΑΝΤΑΦΥΛΛΙΑ:
Ὁδὸς μακρά, ἀλλὰ καλὴ·
ὁ βαδίζων μὴ φοβείσθω.

ἌΝΝΑ:
Ἱστορίαι τὸν δρόμον στολίζουσιν·
ἄνευ αὐτῶν, ἔρημος.

ΧΡΙΣΤΙΝΑ:
Ἔρημος ἄνυδρος τὴν ψυχὴν ἀσθενῆ ποιεῖ.

ἈΛΕΞΑΝΔΡΑ:
Ἡ ποίησις ὕδωρ· ἄνευ ταύτης ὁ ἄνθρωπος ξηραίνεται.

ΜΑΘΗΤΗΣ:
Ἄρα ποίησις πηγὴ εὐδαιμονίας.

ΤΡΙΑΝΤΑΦΥΛΛΙΑ:
Μὴν ὑπερβάλλῃς· ποίησις μόνη οὐκ ἀρκεῖ.

ἌΝΝΑ:
Δεῖ καὶ ἀγάπην, καὶ σιωπήν.

ΧΡΙΣΤΙΝΑ:
Δεῖ καὶ κρίσιν, καὶ φρόνησιν.

ἈΛΕΞΑΝΔΡΑ:
Δεῖ καὶ θάρσος· μὴ φοβηθῇς σεαυτόν.

ΜΑΘΗΤΗΣ:
Πῶς πάντα ταῦτα ἁρμόζεται;

ΤΡΙΑΝΤΑΦΥΛΛΙΑ:
Ὡς χορδαὶ λύρας· ἕκαστον τὸν τόνον ἔχει,
ἀλλὰ ὅλα ἁρμονίαν ποιεῖ.

ἌΝΝΑ:
Ἡ ποίησις λύρα· ἡμεῖς χορδαί.

ΧΡΙΣΤΙΝΑ:
Ἡ λογοτεχνία ἦχος ἐκ τῆς συναντήσεως.

ἈΛΕΞΑΝΔΡΑ:
Ἡ εὐδαιμονία τὸ μέλος·
ὅταν πάντα ἑνῶνται, γίγνεται.

ΜΑΘΗΤΗΣ:
Νῦν μανθάνω ὅτι
ἡ εὐδαιμονία ποίησις βίου ἐστίν.

ΤΡΙΑΝΤΑΦΥΛΛΙΑ:
Εὖ εἴρηκας· ἡ ζωή τέχνη ἐστίν.

ἌΝΝΑ:
Κάθε ἀναπνοὴ στίχος.

ΧΡΙΣΤΙΝΑ:
Κάθε δάκρυ διάλογος χρόνου.

ἈΛΕΞΑΝΔΡΑ:
Κάθε χαμόγελον λύτρωσις·
ἡ ψυχὴ ἔμαθε ᾄδειν.
"""

dialogue_modern = """
ΜΑΘΗΤΗΣ:
Δασκάλα, τι είναι η ευδαιμονία;

ΤΡΙΑΝΤΑΦΥΛΛΙΑ:
Η ευτυχία, παιδί μου, δεν βρίσκεται στα εξωτερικά,
αλλά κατοικεί μέσα στην ψυχή.

ΑΝΝΑ:
Χωρίς ποίηση η ψυχή ξεραίνεται·
με τους λόγους ανθίζει.

ΧΡΙΣΤΙΝΑ:
Η συγκίνηση είναι άνεμος· έρχεται και φεύγει·
η διδασκαλία ριζώνει και μένει.

ΑΛΕΞΑΝΔΡΑ:
Η αλήθεια δίνει δύναμη·
όποιος αγαπά την τέχνη και τον λόγο δεν φοβάται.

ΜΑΘΗΤΗΣ:
Δηλαδή η ποίηση διδάσκει ή μόνο τέρπει;

ΤΡΙΑΝΤΑΦΥΛΛΙΑ:
Και τα δύο, φίλε· διδασκαλία και παρηγοριά είναι ένα.

ΑΝΝΑ:
Η λογοτεχνία είναι σαν κήπος·
άλλοτε δίνει καρπούς, άλλοτε άνθη.

ΧΡΙΣΤΙΝΑ:
Μα κι αυτά τα άνθη δεν είναι ευκαταφρόνητα·
χωρίς αυτά, η καρδιά νεκρώνεται.

ΑΛΕΞΑΝΔΡΑ:
Η ευτυχία είναι καρπός της αρετής.

ΜΑΘΗΤΗΣ:
Και πώς διδάσκεται η αρετή;

ΤΡΙΑΝΤΑΦΥΛΛΙΑ:
Όχι με λόγια μόνο, αλλά με ζωή και έργα.

ΑΝΝΑ:
Κάθε ποίημα είναι μια μικρή πράξη·
σπέρνει μέσα στις ψυχές των ακροατών.

ΧΡΙΣΤΙΝΑ:
Κι ο αναγνώστης γίνεται ποιητής·
χωρίς αυτόν, οι στίχοι μένουν κενοί.

ΑΛΕΞΑΝΔΡΑ:
Η ψυχή που διαβάζει φορεί νέο σώμα.

ΜΑΘΗΤΗΣ:
Νόμιζα πως η ευτυχία είναι υλικό πράγμα·
μα τώρα βλέπω πως είναι δρόμος.

ΤΡΙΑΝΤΑΦΥΛΛΙΑ:
Δρόμος μακρύς, αλλά καλός·
ο βαδίζων ας μην φοβάται.

ΑΝΝΑ:
Ιστορίες στολίζουν τον δρόμο·
χωρίς αυτές, είναι έρημος.

ΧΡΙΣΤΙΝΑ:
Κι η έρημος άνυδρη την ψυχή αρρωσταίνει.

ΑΛΕΞΑΝΔΡΑ:
Η ποίηση είναι νερό·
χωρίς αυτήν, ο άνθρωπος ξεραίνεται.

ΜΑΘΗΤΗΣ:
Άρα η ποίηση είναι πηγή ευτυχίας.

ΤΡΙΑΝΤΑΦΥΛΛΙΑ:
Μην υπερβάλλεις· η ποίηση μόνη της δεν αρκεί.

ΑΝΝΑ:
Χρειάζεται και αγάπη, και σιωπή.

ΧΡΙΣΤΙΝΑ:
Χρειάζεται και κρίση, και φρόνηση.

ΑΛΕΞΑΝΔΡΑ:
Χρειάζεται και θάρρος· να μη φοβηθείς τον εαυτό σου.

ΜΑΘΗΤΗΣ:
Και όλα αυτά πώς ενώνονται;

ΤΡΙΑΝΤΑΦΥΛΛΙΑ:
Σαν χορδές σε λύρα· η καθεμιά έχει τον τόνο της,
μα όλες μαζί φτιάχνουν αρμονία.

ΑΝΝΑ:
Η ποίηση είναι η λύρα· εμείς οι χορδές.

ΧΡΙΣΤΙΝΑ:
Η λογοτεχνία είναι ο ήχος της συνάντησης.

ΑΛΕΞΑΝΔΡΑ:
Η ευτυχία είναι το τραγούδι·
όταν όλα ενωθούν, γεννιέται.

ΜΑΘΗΤΗΣ:
Τώρα μαθαίνω πως
η ευτυχία δεν είναι τίποτε άλλο παρά ποίηση ζωής.

ΤΡΙΑΝΤΑΦΥΛΛΙΑ:
Καλά το είπες· η ζωή είναι τέχνη.

ΑΝΝΑ:
Κάθε ανάσα είναι στίχος.

ΧΡΙΣΤΙΝΑ:
Κάθε δάκρυ διάλογος με τον χρόνο.

ΑΛΕΞΑΝΔΡΑ:
Κάθε χαμόγελο λύτρωση·
η ψυχή έμαθε να τραγουδά.
"""

print("Διάλογος στα ἀρχαῖα ἑλληνικά:\n")
print(dialogue_ancient)
print("\n" + "="*60 + "\n")
print("Διάλογος στὰ νέα ἑλληνικά:\n")
print(dialogue_modern)
